    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
   
        if(username.getText().equals("") || password.getText().equals("")){
             JOptionPane.showMessageDialog(null,"Fill in the required details\nClick OK to continue","Login not success",JOptionPane.ERROR_MESSAGE);
        }else{
            try {
                // TODO add your handling code here:
                String hash_user = Hasher.hash(Txt.append(AdminRecord.getAdminlist().get(0).getRand(), username.getText()),"MD5");
                String hash_pass = Hasher.hash( Txt.append(AdminRecord.getAdminlist().get(0).getRand(), password.getText()),"MD5");
                if(hash_pass.equals(AdminRecord.getAdminlist().get(0).getPassword())
                        && hash_user.equals(AdminRecord.getAdminlist().get(0).getUseranme()))
                {
                    JOptionPane.showMessageDialog(null,"Welcome back, "+AdminRecord.getAdminlist().get(0).getName()+"\nClick OK to continue","Login successfully",JOptionPane.INFORMATION_MESSAGE);
                    setVisible(false);
                    AdminMenu Am = new AdminMenu();
                    Am.setVisible(true);
                }else{
                    JOptionPane.showMessageDialog(null,"Wrong details\nClick OK to continue","Login failed",JOptionPane.ERROR_MESSAGE);

                }
            } catch (Exception ex) {
                
            }
        }
    }//GEN-LAST:event_loginActionPerformed
  
